using UnityEngine;
using UnityEngine.EventSystems;

public class SelectableFixer : MonoBehaviour
{
    public GameObject[] selectableObjects;  // Array ??????????? UI elements ???????????????????????????????
    private int currentIndex = 0;

    void Update()
    {
        // ??????????????? GameObject ?????????????????????? selectableObjects ????
        if (EventSystem.current.currentSelectedGameObject == null && selectableObjects.Length > 0)
        {
            // ????? GameObject ?????????? Array
            EventSystem.current.SetSelectedGameObject(selectableObjects[currentIndex]);
        }
    }

    public void SelectNext()
    {
        // ?????????? currentIndex ??????????????????????????????????????????????????? Array
        currentIndex = (currentIndex + 1) % selectableObjects.Length;
        EventSystem.current.SetSelectedGameObject(selectableObjects[currentIndex]);
    }

    public void SelectPrevious()
    {
        // ?????????? currentIndex ????????????????????????????????????????? Array ???????????????????
        currentIndex = (currentIndex - 1 + selectableObjects.Length) % selectableObjects.Length;
        EventSystem.current.SetSelectedGameObject(selectableObjects[currentIndex]);
    }
}

